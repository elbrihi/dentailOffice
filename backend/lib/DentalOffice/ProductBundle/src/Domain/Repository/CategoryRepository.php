    public function findUniqueCategory(int $id): ?Category
    {
        $query = $this->createQueryBuilder('c')
            ->where('c.id = :id')
            ->setParameter('id', $id)
            ->getQuery();

        try {
            return $query->getOneOrNullResult();
        } catch (NonUniqueResultException $e) {
            throw new \RuntimeException('Duplicate results found, check your database integrity.', 500, $e);
        }
    }
